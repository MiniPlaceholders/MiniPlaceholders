package io.github.miniplaceholders.api.types;

/**
 * Platforms on which MiniPlaceholders can be used.
 *
 * @since 1.0.0
 */
public enum Platform {
  /**
   * Paper Platform
   *
   * @since 1.0.0
   */
  PAPER,
  /**
   * Velocity Platform
   *
   * @since 1.0.0
   */
  VELOCITY,
  /**
   * Fabric Platform
   *
   * @since 2.1.0
   */
  FABRIC,
  /**
   * Sponge Platform
   *
   * @since 2.2.0
   */
  SPONGE
}
